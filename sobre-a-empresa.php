		<h1>VOCÊ CONHECE A HISTÓRIA DA <span class="realce">PRISMA</span>?</h1>
      <div class="subtitulo">
        A Prisma surgiu como um processo pessoal de recolocação<br>
        profissional no mercado a partir do ano de 2016. 
      </div>
	<div class="row margin-bottom">		
		<div class="col-sm historia">
			<p>Oi, meu nome é Anne, eu sou uma brasileira apaixonada por artes, design e tecnologia. Eu iniciei meu caminho na profissão como designer gráfica,
			porém a maior parte da minha carreira foi desenvolvida no mundo digital, com um cargo muito comum a alguns anos atrás: <strong>webdesigner</strong>.
			Durante minha atuação como webdesigner concebi diversos projetos como layout de sites, ícones, e-mails marketing, etc. Com o avanço das linguagens adentrei na era dos sites responsivos, a evolução do front-end para CSS3 e HTML5 e o início de uma nova preocupação: <strong>a experiência do usuário</strong>. 
			Naturalmente minha função evoluiu de projetar sites para produtos digitais utilizando diversos processos e metodologias.</p>
			Com a complexidade que foi tomando conta do meu trabalho, eu me vi em um momento de migração de carreira e transição dentro das empresas e projetos.
			Em 2017 depois de traçar um <a href="https://www.youtube.com/watch?v=AlBZWCRuwAw" target="_blank">plano de disseminação da minha marca pessoal como designer
			</a>, decidi procurar outras cargos que envolvessem esse novo papel voltado para a inovação, consultoria e transformação digital. Assim, analisando meu papel na empresa que me encontrava, me senti deslocada com meus objetivos e decidi pedir demissão e procurar outro cargo em que minhas qualificações seriam melhor aproveitadas e me sentisse mais satisfeita.</p>
			<p>Então, enquanto procurava me recolocar comecei a procurar por trabalhos freelancer, mas para conseguir projetos e clientes, eu precisava
			posicionar e divulgar meus serviços, a partir daí, veio a concepção do papel da entrega de valor através do Design.<br><br>
			<span  class="realce-color-blue">Esse é o papel e a concepção do que veio a se tornar a Prisma: entregar uma estratégia de design que refleta as necessidades do mercado e abarque uma expertise multidisciplinar.</span></p>
